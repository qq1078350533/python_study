package com.snackdeng_04;
/*
    获取数据1-5 5-1
* */
public class forTest {
    public static void main(String[] args) {
        for (int i=1; i<=5; i++) {
            System.out.println(i);
        }

        System.out.println("----------------");

        for (int a=5; a>=1; a--) {
            System.out.println(a);
        }
    }
}
